#include "libft.h"

void				ft_sstkdisp(t_sstack *stk)
{
	size_t	i;

	i = stk->sp;
	while (i > 0)
	{
		ft_putnbr(stk->stack[--i]);
		ft_putchar('\n');
	}
}

#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	s;
	size_t	c;

	s = ft_strlen(s1);
	c = -1;
	while (s2[++c] && c < n)
		s1[s + c] = s2[c];
	s1[s + c] = '\0';
	return (s1);
}
